package com.adyen.commerce.connector.exception;

/**
 * A transient failure (timeout, 5xx, rate limit) where retrying the same operation may succeed.
 */
public class RetryableBillingException extends BillingException
{
	public RetryableBillingException(final String message)
	{
		super(message, true, null);
	}

	public RetryableBillingException(final String message, final Throwable cause)
	{
		super(message, true, cause);
	}
}
