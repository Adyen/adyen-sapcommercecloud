package com.adyen.commerce.connector.exception;

/**
 * A permanent failure (validation, declined, not-found) that will fail again on replay.
 */
public class TerminalBillingException extends BillingException
{
	public TerminalBillingException(final String message)
	{
		super(message, false, null);
	}

	public TerminalBillingException(final String message, final Throwable cause)
	{
		super(message, false, cause);
	}
}
